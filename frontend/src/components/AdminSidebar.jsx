import { useState } from 'react';

/**
 * AdminSidebar - Professional sidebar ສຳລັບ admin dashboard
 *
 * @param {Array} groups - [{title, items: [{key, label, icon, badge}]}]
 * @param {string} activeKey - current active tab key
 * @param {function} onSelect - callback when item clicked
 */
export default function AdminSidebar({ groups, activeKey, onSelect }) {
  const [mobileOpen, setMobileOpen] = useState(false);

  return (
    <>
      {/* Mobile toggle button */}
      <button
        onClick={() => setMobileOpen(!mobileOpen)}
        className="lg:hidden mb-4 flex items-center gap-2 px-4 py-2.5 bg-white border border-gray-200 rounded-xl text-sm font-medium hover:bg-gray-50"
      >
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
          <line x1="3" y1="12" x2="21" y2="12"/><line x1="3" y1="6" x2="21" y2="6"/><line x1="3" y1="18" x2="21" y2="18"/>
        </svg>
        ເມນູ
      </button>

      {/* Sidebar */}
      <aside className={`
        ${mobileOpen ? 'block' : 'hidden lg:block'}
        bg-white rounded-2xl border border-gray-200 p-3
        lg:sticky lg:top-20 lg:self-start lg:max-h-[calc(100vh-6rem)] overflow-y-auto
      `}>
        {groups.map((group, gi) => (
          <div key={gi} className={gi > 0 ? 'mt-4' : ''}>
            <div className="text-[10px] text-gray-400 uppercase tracking-wider font-bold px-3 mb-2">
              {group.title}
            </div>
            <div className="space-y-0.5">
              {group.items.map((item) => {
                const isActive = activeKey === item.key;
                return (
                  <button
                    key={item.key}
                    onClick={() => { onSelect(item.key); setMobileOpen(false); }}
                    className={`w-full flex items-center gap-2.5 px-3 py-2 rounded-lg text-sm font-medium transition-all ${
                      isActive
                        ? 'bg-blue-50 text-blue-700'
                        : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                    }`}
                  >
                    <span className={`w-5 h-5 flex items-center justify-center shrink-0 ${isActive ? 'text-blue-600' : 'text-gray-400'}`}>
                      {item.icon}
                    </span>
                    <span className="flex-1 text-left">{item.label}</span>
                    {item.badge != null && item.badge > 0 && (
                      <span className={`text-[10px] font-bold px-1.5 py-0.5 rounded-full min-w-[18px] text-center ${
                        item.badgeColor === 'red'
                          ? 'bg-red-500 text-white'
                          : item.badgeColor === 'yellow'
                          ? 'bg-yellow-500 text-white'
                          : 'bg-blue-500 text-white'
                      }`}>
                        {item.badge > 99 ? '99+' : item.badge}
                      </span>
                    )}
                  </button>
                );
              })}
            </div>
          </div>
        ))}
      </aside>
    </>
  );
}

